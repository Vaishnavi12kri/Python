#append

list = [2,1,3]
list.append(4)
print(list)

#sort
list = [2,1,3]
list.sort()
print(list)

#list.sort(reverse = True)
list = [2,1,3]
list.sort(reverse=True)
print(list)

#character's sorting
list = ['a','d','e','f','c','b']
list.sort()
print(list)

#reverse method
list = ['a','d','e','f','c','b']
list.reverse()
print(list)

#insert element
list = [2,1,3]
list.insert(1,5)
print(list)

#remove method
list = [2,1,3,1]
list.remove(1)
print(list)

#pop method
list = [2,1,3,1]
list.pop(2)
print(list)